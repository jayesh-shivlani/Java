// Program to print a pattern without using loops
// Demonstrates basic printing of a descending star pattern

public class PrintPattern {
    public static void main(String[] args) {
        // Print first line with 4 stars
        System.out.println("* * * *"); 
        
        // Print second line with 3 stars
        System.out.println("* * *");
        
        // Print third line with 2 stars
        System.out.println("* *");
        
        // Print fourth line with 1 star
        System.out.println("*");
    }
}