// Program to calculate sum of 2 numbers without taking input
// Uses hardcoded values to demonstrate basic addition

public class SumWithoutInput {
    public static void main(String args[]) {
        // Initialize first number
        int a = 10;
        
        // Initialize second number
        int b = 5;
        
        // Calculate sum by adding a and b
        int sum = a + b;
        
        // Display the result
        System.out.println(sum);
    }
}