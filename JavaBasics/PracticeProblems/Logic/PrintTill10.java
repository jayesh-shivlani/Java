// Program to print nummbers from 1 to 10

public class PrintTill10 {

    public static void main(String[] args) {
        // initialize counter with 1
        int counter = 1;

        while (counter <= 10) {
            System.out.print(counter + " ");
            counter++; // updating counter variable
        }
        System.out.println();
    }
}
